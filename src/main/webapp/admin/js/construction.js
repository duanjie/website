/**
  网站建设 js类
 */

$(function(){
		$("#cons").edit();
	});
	jQuery.fn.extend({
		edit : function() {
			var itemFirstTitle = document.getElementById("itemFirstTitle");
			var itemFirstContent = document.getElementById("itemFirstContent");
			var itemFirstTag = document.getElementById("itemFirstTag");
			var itemSecondTitle = document.getElementById("itemSecondTitle");
			var itemSecondContent = document.getElementById("itemSecondContent");
			var itemSecondTag = document.getElementById("itemSecondTag");
			var itemThirdTitle = document.getElementById("itemThirdTitle");
			var itemThirdContent = document.getElementById("itemThirdContent");
			var itemThirdTag = document.getElementById("itemThirdTag");
		
			itemFirstTitle.onblur=function(){
			if (itemFirstTitle.value.length<=0) {
				$("#prompt1").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
				$("#prompt1").className="prompt1";
				return;
			} else {
				$("#prompt1").html("");
			}
		};
		itemFirstContent.onblur=function(){
			if (itemFirstContent.value.length<=0) {
				$("#prompt2").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
				$("#prompt2").className="prompt2";
				return;
			} else {
				$("#prompt2").html("");
			}
		};
		itemFirstTag.onblur=function(){
			if (itemFirstTag.value.length<=0) {
				$("#prompt3").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
				$("#prompt3").className="prompt3";
				return;
			} else {
				$("#prompt3").html("");
			}
		};
		itemSecondTitle.onblur=function(){
			if (itemSecondTitle.value.length<=0) {
				$("#prompt4").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
				$("#prompt4").className="prompt4";
				return;
			} else {
				$("#prompt4").html("");
			}
		};
		itemSecondContent.onblur=function(){
			if (itemSecondContent.value.length<=0) {
				$("#prompt5").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
				$("#prompt5").className="prompt5";
				return;
			} else {
				$("#prompt5").html("");
			}
		};
		itemSecondTag.onblur=function(){
			if (itemSecondTag.value.length<=0) {
				$("#prompt6").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
				$("#prompt6").className="prompt6";
				return;
			} else {
				$("#prompt6").html("");
			}
		};
		itemThirdTitle.onblur=function(){
			if (itemThirdTitle.value.length<=0) {
				$("#prompt7").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
				$("#prompt7").className="prompt6";
				return;
			} else {
				$("#prompt7").html("");
			}
		};
		itemThirdContent.onblur=function(){
			if (itemThirdContent.value.length<=0) {
				$("#prompt8").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
				$("#prompt8").className="prompt8";
				return;
			} else {
				$("#prompt8").html("");
			}
		};
		itemThirdTag.onblur=function(){
			if (itemThirdTag.value.length<=0) {
				$("#prompt9").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
				$("#prompt9").className="prompt9";
				return;
			} else {
				$("#prompt9").html("");
			}
		};
			
			this.find("#submit").click(function() {
				if(itemFirstTitle.value.length<=0||itemFirstTitle.value==null||itemFirstTitle.value.trim()==null) {
					$("#prompt1").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
					$("#prompt1").className="prompt1";
					return false;
				} else if(itemFirstContent.value.length<=0||itemFirstContent.value==null||itemFirstContent.value.trim()==null){
					$("#prompt2").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
					$("#prompt2").className="prompt2";
					return false;
				} else if(itemFirstTag.value.length<=0||itemFirstTag.value==null||itemFirstTag.value.trim()==null){
					$("#prompt3").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
					$("#prompt3").className="prompt3";
					return false;
				} else if(itemSecondTitle.value.length<=0||itemSecondTitle.value==null||itemSecondTitle.value.trim()==null){
					$("#prompt4").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
					$("#prompt4").className="prompt4";
					return false;
				} else if(itemSecondContent.value.length<=0||itemSecondContent.value==null||itemSecondContent.value.trim()==null){
					$("#prompt5").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
					$("#prompt5").className="prompt5";
					return false;
				} else if(itemSecondTag.value.length<=0||itemSecondTag.value==null||itemSecondTag.value.trim()==null){
					$("#prompt6").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
					$("#prompt6").className="prompt6";
					return false;
				} else if(itemThirdTitle.value.length<=0||itemThirdTitle.value==null||itemThirdTitle.value.trim()==null){
					$("#prompt7").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
					$("#prompt7").className="prompt7";
					return false;
				} else if(itemThirdContent.value.length<=0||itemThirdContent.value==null||itemThirdContent.value.trim()==null){
					$("#prompt8").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
					$("#prompt8").className="prompt8";
					return false;
				} else if(itemThirdTag.value.length<=0||itemThirdTag.value==null||itemThirdTag.value.trim()==null){
					$("#prompt9").html("<span style='color:red'>&nbsp;&nbsp;&nbsp;内容不能为空！</span>");
					$("#prompt9").className="prompt9";
					return false;
				}
			});
		}
	});